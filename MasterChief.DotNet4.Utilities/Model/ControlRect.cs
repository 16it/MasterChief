namespace MasterChief.DotNet4.Utilities.Model
{
    /// <summary>
    /// 记录窗体和其控件的初始位置和大小。
    /// </summary>
    public struct ControlRect
    {
        #region Fields

        /// <summary>
        /// 高度
        /// </summary>
        public int Height;

        /// <summary>
        /// 左边
        /// </summary>
        public int Left;

        /// <summary>
        /// 上边
        /// </summary>
        public int Top;

        /// <summary>
        /// 宽度
        /// </summary>
        public int Width;

        #endregion Fields
    }
}